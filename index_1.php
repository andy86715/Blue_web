							<h2>研究目的</h2>
							<p>
								對於社群網路的研究，絕大多是採用問卷調查法進行研究。近年的研究如鄭雅雯（2013）曾對高中生在 Facebook上的自我揭露狀況進行調查，採用便利取樣獲得917個有效樣本，發現自我揭露對 於接受社會支持與提供社會支持，有正向的直接效果，也與正向情感有顯著正相關。不過其自我揭露 狀況、社會支持及正負向情感都是利用量表測得。陳麗芳（2014）則是使用網路問卷取得可用問卷 301份，發現自我揭露及社會支持對於寂寞感有顯著的預測力。	少數的研究直接對訊息內容加以分析，比如說邱銘心、莊曼君（2013）即針對兒童身心障礙之 Facebook社群，以內容分析法對發佈之訊息進行編碼與分析。不過國內現存文獻，並沒有針對社群網 路環境下之憂鬱狀況進行相關的研究。	Poulin,	Shiner,	Thompson	et	al.	(2014）利用機械學習的方式對榮民醫院醫師之臨床筆記進行分析， 在區辨是否有自殺記錄上，訓練結果可達65%以上的正確預測率。研究團隊也利用這個研究成果，在 2013年發起了「涂爾幹計畫」，由退役士兵中徵求受試者，透過應用程式將社交網路服務如Facebook、 Twitter及LinkedIn資訊，同時也加上手機資訊如位置及簡訊，儲存到一個整合式的資料庫，期待能 對自傷行為之風險因子進行即時性地評估（Durkheim	Project,	2013）。	涂爾幹計畫預計徵求10萬名退役士兵，自願讓計畫成員監測所有社群網路訊息，期待能建立即 時預測的模型。不過立意良善的計畫，其雄大的目標正是現實障礙所在，要求參與者放棄個人隱私， 隨時隨地被監察所有社交網路行為，並不是一個可以輕易達成的決定。	我們想提出一個更符合自然情境下的協助計畫，在協助的目標及分析的技術或許大致相同，但盡 可能地利用非干擾的公開訊息，利用原有的支持體系，達到相似之目的
							</p>
							<h2>相關計畫</h2>
							<p>
								相對於涂爾幹計畫主要希望能幫助醫生評估退役士兵的自傷可能性，期待將參與者在社交網路上 之所言所行與正式的醫療記錄進行相關分析，確認風險因子以預測自傷之可能，本研究將利用網路社 群的巨量資料，配合憂鬱症的理論架構，對文本訊息中，能反應憂鬱情緒及憂鬱症狀的使用語彙進行 探索並建構詞彙庫。接著，在這個語彙庫的支持下，對社群網路中的成員發文進行分析，探究這些具 有憂鬱情緒的語詞出現的次數、時間、頻率、強度，以及所牽涉的人物及扮演的角色，藉此了解發文 者與回文者間之互動模式以及找出最具影響力的文章與發文者。最後，利用意見探勘技術建立憂鬱情 緒探測模式，協助找出具有憂鬱情緒的文章與其發文者，同時歸納憂鬱的情緒如何被影響，以及支持 性群體如何被定義
							</p>
